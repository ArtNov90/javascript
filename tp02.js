// Déclaration des variables num1 et num2 avec des valeurs entières
let num1 = 10;
let num2 = 5;

// Opérations arithmétiques
console.log("Addition : " + (num1 + num2));      // Addition
console.log("Soustraction : " + (num1 - num2));  // Soustraction
console.log("Multiplication : " + (num1 * num2)); // Multiplication
console.log("Division : " + (num1 / num2));      // Division
console.log("Modulo : " + (num1 % num2));        // Modulo

// Opérations de comparaison
console.log("num1 > num2 : " + (num1 > num2));   // num1 > num2
console.log("num1 < num2 : " + (num1 < num2));   // num1 < num2
console.log("num1 == num2 : " + (num1 == num2)); // num1 == num2
console.log("num2 != num2 : " + (num2 != num2)); // num2 != num2 (ici, ça renverra false)

// Déclaration des variables booléennes b1 et b2
let b1 = true;
let b2 = false;

// Opérations logiques
console.log("b1 && b2 : " + (b1 && b2)); // b1 AND b2
console.log("b1 || b2 : " + (b1 || b2)); // b1 OR b2
console.log("!b1 : " + (!b1));            // NOT b1
console.log("!b2 : " + (!b2));            // NOT b2